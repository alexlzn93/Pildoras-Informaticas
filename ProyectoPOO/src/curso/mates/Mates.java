// private 

public class Mates {

	public int suma(int a, int b) {
		return a + b;
	}
	
	int suma(float a, float b, float c) {
		return (int)(a + b + c);
	}
	
	int suma(int a) {
		return a + 2;
	}
	
	float suma(float a, float b) {
		return a + b;
	}
	
	double suma(double a, double b) {
		return a + b;
	}
	
	Long suma(Long a, Long b) {
		return a + b;
	}
	
	byte suma(byte a, byte b) {
		return (byte)(a + b);
	}
	
}
